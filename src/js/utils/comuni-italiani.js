let comuniCache = [...COMUNI_INITIAL];

async function loadFullComuniDatabase() {
  try {
    const cached = localStorage.getItem('comuni_db_v3');
    if (cached) {
      comuniCache = JSON.parse(cached);
      return;
    }

    // Carica dal file locale (nessuna connessione internet necessaria)
    const res = await fetch('./js/utils/comuni.json');
    if (res.ok) {
      const data = await res.json();
      if (Array.isArray(data) && data.length > 0) {
        const fullList = data.map(item => ({
          nome: item.nome,
          cap: (item.cap && item.cap.length ? item.cap[0] : '00000'),
          provincia: item.sigla || (item.provincia ? item.provincia.sigla : ''),
          regione: item.regione ? item.regione.nome : '',
          catastale: item.codiceCatastale || ''
        }));
        comuniCache = fullList;
        localStorage.setItem('comuni_db_v3', JSON.stringify(fullList));
      }
    }
  } catch (e) {
    // Usa la lista iniziale integrata come fallback
    console.log('comuni-italiani: uso lista incorporata come fallback');
  }
}

loadFullComuniDatabase();

export function searchComuni(query) {
  if (!query || query.trim().length < 2) return [];
  const q = query.trim().toLowerCase();
  
  return comuniCache.filter(c =>
    c.nome.toLowerCase().includes(q) ||
    (c.cap && c.cap.startsWith(q)) ||
    (c.provincia && c.provincia.toLowerCase() === q)
  ).slice(0, 10);
}

export function getComuneByNome(nome) {
  if (!nome) return null;
  const n = nome.trim().toLowerCase();
  return comuniCache.find(c => c.nome.toLowerCase() === n) || null;
}

export function bindComuneAutocomplete(inputEl, onSelect) {
  if (!inputEl) return;

  let suggBox = document.createElement('div');
  suggBox.className = 'comuni-sugg-box';
  suggBox.style.cssText = 'position:absolute;top:100%;left:0;right:0;background:var(--bg-card);border:1px solid var(--border);border-radius:8px;box-shadow:0 6px 16px rgba(0,0,0,0.15);z-index:1000;max-height:220px;overflow-y:auto;display:none;margin-top:2px;';
  
  if (inputEl.parentElement) {
    if (getComputedStyle(inputEl.parentElement).position === 'static') {
      inputEl.parentElement.style.position = 'relative';
    }
    inputEl.parentElement.appendChild(suggBox);
  }

  let searchTimeout;
  inputEl.addEventListener('input', () => {
    const q = inputEl.value.trim();
    if (searchTimeout) clearTimeout(searchTimeout);

    if (q.length < 2) {
      suggBox.style.display = 'none';
      return;
    }

    searchTimeout = setTimeout(() => {
      const list = searchComuni(q);
      if (list.length === 0) {
        suggBox.style.display = 'none';
        return;
      }

      suggBox.innerHTML = list.map(c => `
        <div class="comune-item" style="padding:8px 12px;cursor:pointer;border-bottom:1px solid var(--border);font-size:13px;display:flex;justify-content:space-between;" data-comune='${JSON.stringify(c).replace(/'/g, "&apos;")}'>
          <span style="font-weight:600">${c.nome} (${c.provincia})</span>
          <span class="td-mono" style="color:var(--text-muted);font-size:11px">CAP ${c.cap} | ${c.catastale}</span>
        </div>
      `).join('');
      suggBox.style.display = 'block';

      suggBox.querySelectorAll('.comune-item').forEach(item => {
        item.addEventListener('click', () => {
          const c = JSON.parse(item.getAttribute('data-comune').replace(/&apos;/g, "'"));
          inputEl.value = c.nome;
          suggBox.style.display = 'none';
          if (typeof onSelect === 'function') onSelect(c);
        });
        item.addEventListener('mouseenter', () => item.style.background = 'var(--bg-hover)');
        item.addEventListener('mouseleave', () => item.style.background = 'transparent');
      });
    }, 150);
  });

  document.addEventListener('click', (e) => {
    if (!inputEl.contains(e.target) && !suggBox.contains(e.target)) {
      suggBox.style.display = 'none';
    }
  });
}
